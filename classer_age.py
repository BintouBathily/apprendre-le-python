# Fonction qui classe une personne selon son âge

def classer_age(age):
    if age < 0 or age > 120:
        print("Âge invalide")
    elif age < 12:
        print("Tu es un enfant")
    elif age <= 17:
        print("Tu es un adolescent")
    elif age <= 64:
        print("Tu es un adulte")
    else:
        print("Tu es un senior")
